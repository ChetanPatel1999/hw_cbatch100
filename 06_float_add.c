//wap to add two float numbers.
#include<stdio.h>
void main()
{
	float num1,num2,sum;
	num1=10.3;
	num2=5.6;
	printf("value of num1 = %.1f\n",num1);
	printf("value of num2 = %.1f\n",num2);
	sum=num1+num2;
	printf("sum = %.1f\n",sum);
	printf("sum of %.1f and %.1f = %.1f\n",num1,num2,sum);
	printf("%.1f + %.1f = %.1f",num1,num2,sum);
}
