//wap to add two integer numbers.
#include<stdio.h>
void main()
{
	int num1,num2,sum;
	num1=567;
	num2=33;
	printf("value of num1 = %d\n",num1);
	printf("value of num2 = %d\n",num2);
	sum=num1+num2;
	printf("sum = %d\n",sum);
	printf("sum of %d and %d = %d\n",num1,num2,sum);
	printf("%d + %d = %d",num1,num2,sum);
}
